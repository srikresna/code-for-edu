package jobsheetCollection.assignment;

public class Mahasiswa {
    String nim, nama, telf;

    public Mahasiswa(String nim, String nama, String telf) {
        this.nim = nim;
        this.nama = nama;
        this.telf = telf;
    }

    public String getNim() {
        return nim;
    }

    public String getNama() {
        return nama;
    }

    public String getTelf() {
        return telf;
    }

}
